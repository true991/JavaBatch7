package com.syntax.class01;

public class Variables {
	public static void main (String [] args) {
		
		/* byte
		 * short
		 * long
		 * int
		 * double
		 * float
		 * boolean
		 * char
		 */
		
		// Whole number data types
		byte num = 127;
		short num1 = 10000;
		int num2 = 10000000;
		long num3 = 10646545600000L; // l or L
		
		// Decimal number data types
		float num4 = 12.9f;  //f or F
		double num5 = 0.888;
		
		// Non-number data types
		char add = '+';
		char letter = 'a';
		
		// True or false (yes or no) ; DO NOT PUT FOR VALUE
		boolean hungry = true;
		boolean thirsty = false;
		
		
	}

}

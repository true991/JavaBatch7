package com.syntax.class02;

public class DoubleComparison {
	public static void main(String[] args) {
		double i = 5.75;
		double j = 15.8;

		if (i > j) {
			System.out.println("Double value " + i + " is larger than " + j);
		} else {
			System.out.println("Double value " + i + " is smaller than " + j);
		}
	}

}

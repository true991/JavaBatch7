package com.syntax.class02;

public class PrimitiveCasting {
	public static void main(String[] args) {

		//widening
		double d = 7;
		System.out.println(d);
		
		int i = (int) 7.58;
		System.out.println(i);
		
		byte b = (byte) 1238016;
		System.out.println(b);
		
		long l1 = 45;
		long l2 = 2323423432432L;
		
		int number = (int) l2;
		System.out.println(number);
		
		
		

	}

}
